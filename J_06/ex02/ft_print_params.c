void	ft_putchar(char c);

void	ft_print_params(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}

int		main(int argc, char **argv)
{
	int n;

	n = 1;
	while (n < argc)
	{
		ft_print_params(argv[n]);
		n++;
	}
	return (0);
}
