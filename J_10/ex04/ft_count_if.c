int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int ct;

	i = 0;
	ct = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
			ct++;
		i++;
	}
	return (ct);
}
