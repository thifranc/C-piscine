char	*ft_strncat(char *dest, char *src, int nb)
{
	int i2;
	int i;

	i = 0;
	i2 = 0;
	while (dest[i2] != '\0')
		i2++;
	while (src[i] != '\0' && i <= nb)
	{
		dest[i2] = src[i];
		i2++;
		i++;
	}
	dest[i2 + 1] = '\0';
	return (dest);
}
